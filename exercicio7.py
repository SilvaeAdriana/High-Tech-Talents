'''
Faça um programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.
'''
print(48 * '*')
print('*** Calcule o dobro da área de um quadrado. ***')
lado = int(input('Digite aqui o tamanho do lado do quadrado que você quer descobrir a área: '))
